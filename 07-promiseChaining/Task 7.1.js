/**
 * Створіть ланцюжок, який:
 * 1. Починається з числа 5
 * 2. Множить на 2
 * 3. Додає 10
 * 4. Конвертує в рядок
 */
function simpleChain() {
    return Promise.resolve(5)
        .then(num => num * 2)        // Отримуємо 5, множимо на 2 (результат 10)
        .then(num => num + 10)       // Отримуємо 10, додаємо 10 (результат 20)
        .then(num => num.toString()); // Отримуємо 20, робимо з нього рядок "20"
}

// Перевірка:
simpleChain()
    .then(result => console.log('Тест 7.1:', result)); // "20"